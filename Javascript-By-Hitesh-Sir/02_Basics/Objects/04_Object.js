//! Methods of Objects.

/*
    ^  1.  Object.freeze(obj_name)
     *        -> method is used to freeze the object to prevent further changes in properties and values.

    ^   2. Object.keys(obj_name)
     *       ->  only the keys of the object.

    ^   3. Object.values(obj_name)
     *       -> only the values of the object.

    ^   4. Object.entries(obj_name)
     *       -> converts object into array.

    ^   5. obj_name.hasOwnProperty('isLoggedIn')
     *       -> returns Boolean value weather isLoggedIn is a property of Obj_name of not?
 */